  static Map<String, Map<String, String>> get dashboardKpis {
    final march = invoices.where((i) => i.issueDate.month == 3).toList();
    final feb = invoices.where((i) => i.issueDate.month == 2).toList();

    final marchTotal = march.fold<double>(0, (s, i) => s + i.total);
    final febTotal = feb.fold<double>(0, (s, i) => s + i.total);

    final marchCount = march.length;
    final febCount = feb.length;

    String format(double v) {
      final text = v.toStringAsFixed(0);
      return text.replaceAllMapped(
        RegExp(r'(\d{1,3})(?=(\d{3})+(?!\d))'),
        (Match m) => '${m[1]}.',
      );
    }

    final countGrowth = febCount == 0
        ? 0
        : ((marchCount - febCount) / febCount * 100).round();
    final revGrowth = febTotal == 0
        ? 0
        : ((marchTotal - febTotal) / febTotal * 100).round();

    final pendingList = invoices
        .where((i) => i.status == InvoiceStatus.pendingReview)
        .toList();
    final pendingTotal = pendingList.fold<double>(0, (s, i) => s + i.total);

    final overdueList = invoices
        .where((i) => i.status == InvoiceStatus.rejected)
        .toList();

    return {
      'emitted': {
        'value': '$marchCount',
        'trend': '${countGrowth >= 0 ? '+' : ''}$countGrowth% vs mes anterior',
      },
      'revenue': {
        'value': '${format(marchTotal)} \u20ac',
        'trend': '${revGrowth >= 0 ? '+' : ''}$revGrowth% vs mes anterior',
      },
      'pending': {
        'value': '${pendingList.length}',
        'trend': '${format(pendingTotal)} \u20ac en total',
      },
      'overdue': {
        'value': '${overdueList.length}',
        'trend': 'Atención requerida',
      },
    };
  }

